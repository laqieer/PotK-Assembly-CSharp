        Debug.Log((object) string.Format("Language '{0}' not recognized. Please, add the language code to GoogleTranslation.cs", (object) Filter));
      return string.Empty;
    }

    public static List<string> GetLanguagesForDropdown(string Filter, string CodesToExclude)
    {
      string[] Filters = Filter.ToLowerInvariant().Split(" /(),".ToCharArray());
      List<string> languagesForDropdown = new List<string>();
      foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
      {
        if (string.IsNullOrEmpty(Filter) || GoogleLanguages.LanguageMatchesFilter(keyValuePair.Key, Filters))
        {
          string str = "[" + keyValuePair.Value.Code + "]";
          if (!CodesToExclude.Contains(str))
            languagesForDropdown.Add(keyValuePair.Key + " " + str);
        }
      }
      for (int index = languagesForDropdown.Count - 2; index >= 0; --index)
      {
        string str = languagesForDropdown[index].Substring(0, languagesForDropdown[index].IndexOf(" ["));
        if (languagesForDropdown[index + 1].StartsWith(str))
        {
          languagesForDropdown[index] = str + "/" + languagesForDropdown[index];
          languagesForDropdown.Insert(index + 1, str + "/");
        }
      }
      return languagesForDropdown;
    }

    public static string GetClosestLanguage(string Filter)
    {
      if (string.IsNullOrEmpty(Filter))
        return string.Empty;
      string[] Filters = Filter.ToLowerInvariant().Split(" /(),".ToCharArray());
      foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
      {
        if (GoogleLanguages.LanguageMatchesFilter(keyValuePair.Key, Filters))
          return keyValuePair.Key;
      }
      return string.Empty;
    }

    private static bool LanguageMatchesFilter(string Language, string[] Filters)
    {
      Language = Language.ToLowerInvariant();
      int index = 0;
      for (int length = Filters.Length; index < length; ++index)
      {
        if (Filters[index] != string.Empty)
        {
          if (!Language.Contains(Filters[index].ToLower()))
            return false;
          Language = Language.Remove(Language.IndexOf(Filters[index]), Filters[index].Length);
        }
      }
      return true;
    }

    public static string GetFormatedLanguageName(string Language)
    {
      string empty = string.Empty;
      int length1 = Language.IndexOf(" [");
      if (length1 > 0)
        Language = Language.Substring(0, length1);
      int length2 = Language.IndexOf('/');
      if (length2 > 0)
      {
        string formatedLanguageName = Language.Substring(0, length2);
        if (Language == formatedLanguageName + "/" + formatedLanguageName)
          return formatedLanguageName;
        Language = Language.Replace("/", " (") + ")";
      }
      return Language;
    }

    public static string GetCodedLanguage(string Language, string code)
    {
      string languageCode = GoogleLanguages.GetLanguageCode(Language);
      return string.Compare(code, languageCode, StringComparison.OrdinalIgnoreCase) == 0 ? Language : Language + " [" + code + "]";
    }

    public static void UnPackCodeFromLanguageName(
      string CodedLanguage,
      out string Language,
      out string code)
    {
      if (string.IsNullOrEmpty(CodedLanguage))
      {
        Language = string.Empty;
        code = string.Empty;
      }
      else
      {
        int num = CodedLanguage.IndexOf("[");
        if (num < 0)
        {
          Language = CodedLanguage;
          code = GoogleLanguages.GetLanguageCode(Language);
        }
        else
        {
          Language = CodedLanguage.Substring(0, num).Trim();
          code = CodedLanguage.Substring(num + 1, CodedLanguage.IndexOf("]", num) - num - 1);
        }
      }
    }

    public static string GetGoogleLanguageCode(string InternationalCode)
    {
      foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
      {
        if (InternationalCode == keyValuePair.Value.Code)
          return string.IsNullOrEmpty(keyValuePair.Value.GoogleCode) ? InternationalCode : keyValuePair.Value.GoogleCode;
      }
      return InternationalCode;
    }

    public static List<string> GetAllInternationalCodes()
    {
      HashSet<string> collection = new HashSet<string>();
      foreach (KeyValuePair<string, GoogleLanguages.LanguageCodeDef> keyValuePair in GoogleLanguages.mLanguageDef)
        collection.Add(keyValuePair.Value.Code);
      return new List<string>((IEnumerable<string>) collection);
    }

